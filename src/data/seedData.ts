export const projects: Project[] = projectSeeds.map((project) => ({
  ...project,
  startDate: addDays((project.startWeek - 1) * 7),
  endDate: addDays((project.startWeek + project.durationWeeks - 1) * 7),
}));

export const cashEvents: CashEvent[] = projects.flatMap((project) => {
  const procurementWeek = clampWeek(project.startWeek);
  const milestoneWeek = clampWeek(project.startWeek + Math.ceil(project.durationWeeks * 0.45));
  const completionWeek = clampWeek(project.startWeek + project.durationWeeks);
  const procurement = Math.round(project.contractValue * 0.24);
  const milestone = Math.round(project.contractValue * 0.38);
  const completion = Math.round(project.contractValue * 0.46);

  return [
    {
      id: `${project.id}-materials`,
      projectId: project.id,
      week: procurementWeek,
      type: "outflow",
      label: "Materials & subcontractors",
      amount: procurement,
    },
    {
      id: `${project.id}-milestone`,
      projectId: project.id,
      week: milestoneWeek,
      type: "inflow",
      label: "Watertight milestone invoice",
      amount: milestone,
    },
    {
      id: `${project.id}-completion`,
      projectId: project.id,
      week: completionWeek,
      type: "inflow",
      label: "Completion invoice",
      amount: completion,
    },
  ];
});

const coastalCities = new Set([
  "Amsterdam",
  "Haarlem",
  "Zaandam",
  "Den Haag",
  "Leiden",
  "Delft",
  "Rotterdam",
  "Schiedam",
  "Dordrecht",
]);

const southernCities = new Set(["Eindhoven", "Tilburg", "Breda", "Den Bosch", "Maastricht", "Venlo", "Heerlen", "Roermond"]);

const cityNames = Array.from(new Set(projects.map((project) => project.city)));

function weatherForCityWeek(city: string, week: number): WeatherForecast {
  const coastal = coastalCities.has(city);
  const south = southernCities.has(city);
  const east = ["Zwolle", "Enschede", "Deventer", "Apeldoorn"].includes(city);

  const stormProfile = [0, 44, 26, 13, 10, 4, 7, 11, 32, 18, 9, 12, 16, 10][week] ?? 10;
  const heatProfile = [0, 22, 24, 26, 28, 34, 31, 25, 23, 21, 27, 29, 24, 22][week] ?? 22;
  const rainMm = Math.max(
    0,
    Math.round(stormProfile + (coastal ? 9 : 0) + (east ? 3 : 0) - (south ? 4 : 0) + ((week * city.length) % 5)),
  );
  const windGustKmh = Math.round(
    30 + (coastal ? 17 : 5) + (week === 1 ? 25 : 0) + (week === 8 ? 19 : 0) + ((city.charCodeAt(0) + week) % 7),
  );
  const maxTempC = Math.round(heatProfile + (south ? 3 : 0) + (coastal ? -1 : 0));
  const minTempC = Math.round(8 + (week % 5) + (south ? 2 : 0) - (east ? 1 : 0));
  const precipProbability = Math.min(96, Math.round(38 + rainMm * 1.1 + (coastal ? 4 : 0)));

  return {
    city,
    week,
    rainMm,
    windGustKmh,
    maxTempC,
    minTempC,
    precipProbability,
    source: "seed",
  };
}

export const weatherForecast: WeatherForecast[] = cityNames.flatMap((city) =>
  Array.from({ length: 13 }, (_, index) => weatherForCityWeek(city, index + 1)),
);

